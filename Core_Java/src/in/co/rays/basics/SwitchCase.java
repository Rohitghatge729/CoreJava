package in.co.rays.basics;

public class SwitchCase {
	public static void main(String[] args) {
		String name = "Rohit";
		switch (name) {
		case "Sanjana":
			System.out.println("Student");
			break;
		case "Bhavesh":
			System.out.println("Collegue");
			break;
		default:
			System.out.println("not found");

		}
	}
}